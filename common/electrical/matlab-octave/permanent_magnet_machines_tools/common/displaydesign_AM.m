function displaydesign_AM(design, simoptions)

    data = [ {'Rc'; 'Lc'; 'FF';'No. Stages';}, ...
             {design.CoilResistance; design.CoilInductance; design.fillfactor; design.NStages;}, ...
             {'Wire D'; 'Coil Turns'; 'RgVRc'; 'No. Strands';}, ...
             {design.Dc; design.CoilTurns; design.RgVRc; design.NStrands;}, ...
             {'Branches'; 'CoilsPerBranch'; 'No. Machines'; ''}, ...
             {design.Branches; design.CoilsPerBranch; simoptions.NoOfMachines; []}, ...
           ];
        
